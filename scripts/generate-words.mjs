// Fisher-Yates shuffle
function shuffle(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

// Generate vocabulary questions
function generateVocabQuestions(count = 150) {
  const selected = shuffle(WORD_LIST).slice(0, count);
  const questions = [];
  let idx = 1;

  for (const [word, def] of selected) {
    // Get 3 random wrong definitions from other words
    const others = WORD_LIST.filter(w => w[0] !== word).map(w => w[1]);
    const wrongDefs = shuffle(others).slice(0, 3);

    // Combine correct + wrong and shuffle
    const allOptions = shuffle([def, ...wrongDefs]);
    const correctIndex = allOptions.indexOf(def);

    questions.push({
      id: `vocab_gen_${String(idx).padStart(3, '0')}`,
      type: 'vocabulary',
      prompt: `"${word}" 最接近的意思是？`,
      options: allOptions,
      answer: correctIndex,
      explanation: `${word} 意为"${def}"。`,
      sourceEvent: idx % 2 === 0 ? '对线补兵' : idx % 3 === 0 ? '敌方消耗' : '1v1单杀',
      difficulty: idx % 10 === 0 ? 3 : idx % 5 === 0 ? 2 : 1,
    });
    idx++;
  }

  return questions;
}

const questions = generateVocabQuestions(150);

